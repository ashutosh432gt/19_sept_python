#Write a Python function to get the largest number ,smallest num and sum of all from a list
numbers =[1,4,6,7,9,3,2,10,11] #creating a list of numbers
largest = max(numbers) #max function to find maximum or largest numbers from the list
print("Largest Number = ",largest) 
smallest = min(numbers) #min function to find the minimum or smallest number from the list
print("Smallest Number = ",smallest)
total = sum(numbers) #sum functon to calculate sum of all numbers from a list
print("Sum of all numbers from list = ",total)   
